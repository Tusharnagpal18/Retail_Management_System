             float nwcesAmt=purRate*cesPer/100;
             gsta.setText(""+nwgstAmt);
             cessa.setText(""+nwcesAmt);
               System.out.println(""+nwgstAmt);
               System.out.println(""+nwcesAmt);
               float nwnettot=nwgstAmt+nwcesAmt;
                float nwNetRate=netRate+nwnettot;                
             netr.setText(""+nwNetRate);
             
               //NET AMOUNT //
               
               int qtti=Integer.valueOf(qty.getText());
